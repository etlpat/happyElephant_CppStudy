// 栈(Stack)
//
//
// (1)概念
//	栈：栈是一种操作受限的线性表，只允许在指定的一端(栈顶)进行插入或删除操作
//	栈顶：栈的顶端，只能在栈顶进行入栈(压栈)或出栈(弹栈)的操作
//	栈底：栈的底端
//
//
// (2)特点
//	栈的特点：先进后出，后进先出
//			  last in fist out（LIFO）
//	
//
// (3)基本操作
//	initStack(&S)：初始化栈
//	destroyStack(&S)：销毁栈
//	push(&S, elem)：入栈
//	pop(&S, &elem)：出栈
//	getTop(&S, &elem)：读取栈顶元素
//	......